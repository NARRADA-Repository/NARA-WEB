						<p>Hydrographic surveying is the science of measuring and describing the physical features of ocean and water bodies. The preliminary task is to measure the bathymetry as a main task in order to do all the next level analysis and decision making procedures. ECHO sounders, GNSS, GPS, SVP and motion sensors are mainly used in bathymetric survey tasks by hydrographic surveyors in the field data collection process. </p>
<p>
  <b>Hydrographic Division was established in 1984, and is accredited to the International Hydrographic Organization (IHO) as the focal point for Hydrography in Sri Lanka.</b>
</p>
  <p>The principle service of the Hydrographic Division carrying out hydrographic surveys through systematic data collection of inshore, near shore, and offshore. These activities in the sea cover up to the limit of Exclusive Economic Zone of Sri Lanka. In addition, hydrographic surveys of Inland water bodies are carried out too. HD produces and disseminates information in support of maritime navigation safety and marine environment preservation, defense, exploration, and research & management plans.</p>
<p>The immediate outputs of the HD are nautical charts, thematic maps on fisheries and other user oriented hydrographic maps involving digital & analogue hydrographic data.</p>
<p>Hydrographic data obtained from surveys, is mainly used for port & harbour development, coastal zone management, delimitation of the limits of national maritime jurisdiction, control of marine pollution, provision of data for coast conservation and coastal engineering projects and provision of charts for navigation. Presently the HD is capable of carrying out hydrographic surveys hydrographic data to a depth of 5000 m, which is well within the continental margin of Sri Lanka</p>

